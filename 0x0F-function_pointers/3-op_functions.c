#include "3-calc.h"
/**
  *op_add - add two integers
  *
  *@a: operand 1
  *@b: operand 2
  *Return: sum of 1 and 2
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  *op_sub - a - b
  *
  *@a: operand 1
  *@b: operand 2
  *Return: difference
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  *op_mul- multiply  a by b
  *
  *@a: operand 1
  *@b: operand 2
  *Return: a * b
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  *op_div - divide
  *@a: operand 1
  *@b: operad 2
  *Return: a / b
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  *op_mod-a % b
  *
  *@a: operand 1
  *@b: operand 2
  *Return: a modulus b
  */
int op_mod(int a, int b)
{
	return (a % b);
}


